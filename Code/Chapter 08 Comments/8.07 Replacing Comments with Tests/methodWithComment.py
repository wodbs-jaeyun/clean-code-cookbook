def multiply(a, b):
    # 이 함수는 두 숫자를 곱한 결과를 반홥합니다.
    # 만약 숫자 중 하나가 0이면 결과는 0입니다.
    # 만약 두 숫자가 모두 양수이면 결과는 양수입니다.
    # 만약 두 숫자가 모두 음수이면 결과는 양수입니다.
    # 곱셈은 기본 연산자를 호출해 수행됩니다.
    return a * b

